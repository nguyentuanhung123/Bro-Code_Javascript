// string method = allow you to manipulate and work with text (strings)

// charAt charAt() trả về ký tự tại chỉ mục (vị trí) được chỉ định trong chuỗi.
// indexOf trả về vị trí xuất hiện đầu tiên của một giá trị trong chuỗi.
// indexOf trả về chỉ mục lần xuất hiện cuối cùng của một giá trị được chỉ định trong chuỗi.

let userName = "BroCode";

console.log(userName.charAt(2)); // o
console.log(userName.indexOf("o")); // 2
console.log(userName.lastIndexOf("o")); // 4
console.log(userName.length); // 7